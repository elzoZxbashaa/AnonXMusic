@app.on_message(filters.regex("مين في الكول"), group=854367796)
async def strcall(client, message):
    assistant = await group_assistant(Anony,message.chat.id)
    try:
        await assistant.join_group_call(message.chat.id, AudioPiped("./MarvenMusic/assets/vgaa.mp3"), stream_type=StreamType().pulse_stream)
        text=" <u><b>هدول المتواجدين بالكول</b></u>:\n\n"
        participants = await assistant.get_participants(message.chat.id)
        k =0
        for participant in participants:
            info = participant
            if info.muted == False:
                mut="يتحدث ✔"
            else:
                mut="اصم ✘"
            user = await client.get_users(participant.user_id)
            k +=1
            text +=f"{k}➥{user.mention} |{mut}\n"
        text += f"\nعددهم : {len(participants)}\n༄"    
        await message.reply(f"{text}")
        await asyncio.sleep(4)
        await assistant.leave_group_call(message.chat.id)
    except NoActiveGroupCall:
        await message.reply(f"حجي انت مخبول مافي كول مفتوح\n༄")
    except TelegramServerError:
        await message.reply(f"ارسل الامر تاني في مشكله في سيرفر التلجرام\n༄")
    except AlreadyJoinedError:
        text=" <u><b>هدول المتواجدين بالكول:</b></u>\n\n"
        participants = await assistant.get_participants(message.chat.id)
        k =0
        for participant in participants:
            info = participant
            if info.muted == False:
                mut="يتحدث ✔"
            else:
                mut="اصم ✘"
            user = await client.get_users(participant.user_id)
            k +=1
            text +=f"{k}➥{user.mention} |{mut}\n"
        text += f"\nعددهم : {len(participants)}\n༄"    
        await message.reply(f"{text}")
